def main():
    # Input a number from the user
    num = float(input("Enter a number: "))
    
    # Multiply by 2
    multiplied_result = num * 2
    
    # Divide by 10
    final_result = multiplied_result / 10
    
    # Print the final result
    print(f"The result of ({num} * 2) / 10 is: {final_result}")

# Execute the main function
if __name__ == "__main__":
    main()